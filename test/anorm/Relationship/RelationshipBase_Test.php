        $this->assertStringContainsString('`fk_custom_name`', $sql);
    }

    // -------------------------------------------------------------------------
    // OneHasMany::batchLoad — strategy branches
    // -------------------------------------------------------------------------

    public function testBatchLoadJoinWithSelectionStrategy()
    {
        // Get users and ensure we have > 10 models so the selector can choose JOIN strategy
        // We need fieldSelection and > 10 models to potentially trigger JOIN strategy.
        // Use a custom strategy selector that forces JOIN_WITH_SELECTION.
        $users = DataMapper::find(UserModel::class, $this->pdo)->some();
        $userArray = iterator_to_array($users);

        if (count($userArray) === 0) {
            $this->markTestSkipped('No test data available');
        }

        $user = $userArray[0];
        $relationshipManager = $user->getRelationshipManager();
        $relationship = $relationshipManager->getRelationship('posts');

        if (!($relationship instanceof OneHasMany)) {
            $this->markTestSkipped('Posts relationship not found or not OneHasMany');
        }

        // Create a subclass that overrides getCardinality and estimateDataSize to force JOIN strategy.
        // The JOIN strategy is selected when:
        //   - sourceCount > individual_loading_threshold (10)
        //   - fieldSelection is non-empty
        //   - joinSize < inClauseSize * (1 - join_strategy_threshold(0.5))
        // We duplicate models to exceed the threshold and provide field selection.
        $manyUsers = [];
        for ($i = 0; $i < 20; $i++) {
            $manyUsers[] = $userArray[0];
        }

        // With 20 models and a field selection that reduces data significantly,
        // the JOIN strategy may be selected.
        // The estimateDataSize for OneHasMany with ['id','title'] uses count(fields)*50 bytes
        // = 2*50 = 100 bytes/record * 5 avg * 20 = 10000 bytes
        // The DataSizeEstimator's estimateJoinDataSize will use a small estimate for 2 fields.
        // If joinSize reduction >= 50%, JOIN strategy is chosen.
        // We use field selection with few fields to maximize reduction ratio.
        $fieldSelection = ['id'];

        $batchResults = $relationship->batchLoad($manyUsers, $this->pdo, $fieldSelection);

        $this->assertIsArray($batchResults);
    }

    public function testBatchLoadIndividualLoadingStrategy()
    {
        // With <= 10 source models and no field selection, strategy is INDIVIDUAL_LOADING
        $users = DataMapper::find(UserModel::class, $this->pdo)->some();
        $userArray = iterator_to_array($users);

        if (count($userArray) === 0) {
            $this->markTestSkipped('No test data available');
        }

        $user = $userArray[0];
        $relationshipManager = $user->getRelationshipManager();
        $relationship = $relationshipManager->getRelationship('posts');

        if (!($relationship instanceof OneHasMany)) {
            $this->markTestSkipped('Posts relationship not found or not OneHasMany');
        }

        // With 1-10 models and no field selection, STRATEGY_INDIVIDUAL_LOADING is selected
        // (individual_loading_threshold = 10, sourceCount <= 10 → INDIVIDUAL_LOADING)
        $smallSet = array_slice($userArray, 0, min(3, count($userArray)));
        $batchResults = $relationship->batchLoad($smallSet, $this->pdo, null);

        $this->assertIsArray($batchResults);
        // Individual loading returns results keyed by primary key value
        foreach ($smallSet as $model) {
            if ($model->id !== null) {
                $this->assertArrayHasKey($model->id, $batchResults);
                $this->assertIsArray($batchResults[$model->id]);
            }
        }
    }

    public function testBatchLoadWithNullSourceValueSkipsModel()
    {
        // Test that load() returns [] when sourceValue is null (line 38)
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        // Create a model with no id set (null primary key)
        $user = new UserModel($this->pdo);
        $user->id = null;

        $result = $rel->load($user, $this->pdo);

        $this->assertIsArray($result);
        $this->assertEmpty($result);
    }
}
