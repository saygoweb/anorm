<?php

namespace Anorm\Test;

use Anorm\DataMapper;
use Anorm\Relationship\OneHasMany;
use Anorm\Relationship\Strategy\QueryStrategySelector;
use PHPUnit\Framework\TestCase;

class RelationshipBase_Test extends TestCase
{
    private $pdo;

    public static function setUpBeforeClass(): void
    {
        TestEnvironment::connect();
        $pdo = TestEnvironment::pdo();

        $pdo->exec('SET FOREIGN_KEY_CHECKS=0');

        $sql = file_get_contents(__DIR__ . '/../RelationshipTestSchema.sql');
        $lines = explode("\n", $sql);
        $cleanSql = '';
        foreach ($lines as $line) {
            $line = trim($line);
            if (!empty($line) && strpos($line, '#') !== 0) {
                $cleanSql .= $line . "\n";
            }
        }

        foreach (explode(';', $cleanSql) as $statement) {
            $statement = trim($statement);
            if (!empty($statement)) {
                try {
                    $pdo->exec($statement);
                } catch (\PDOException $e) {
                    // Ignore errors during schema setup (e.g., table already exists)
                }
            }
        }

        $pdo->exec('SET FOREIGN_KEY_CHECKS=1');
    }

    protected function setUp(): void
    {
        $this->pdo = TestEnvironment::pdo();
    }

    // -------------------------------------------------------------------------
    // Relationship base class: getOptions(), getOption(), getConstraintName()
    // -------------------------------------------------------------------------

    public function testGetOptionsReturnsOptionsArray()
    {
        $options = ['constraints' => ['on_delete' => 'CASCADE', 'on_update' => 'CASCADE']];
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id', $options);

        $result = $rel->getOptions();

        $this->assertIsArray($result);
        $this->assertArrayHasKey('constraints', $result);
    }

    public function testGetOptionsReturnsEmptyArrayWhenNoOptions()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        $result = $rel->getOptions();

        $this->assertIsArray($result);
        $this->assertEmpty($result);
    }

    public function testGetOptionReturnsValueForExistingKey()
    {
        $options = ['constraints' => ['on_delete' => 'CASCADE'], 'custom_key' => 'custom_value'];
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id', $options);

        $value = $rel->getOption('custom_key');

        $this->assertEquals('custom_value', $value);
    }

    public function testGetOptionReturnsDefaultForMissingKey()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        $result = $rel->getOption('nonexistent_key', 'fallback_value');

        $this->assertEquals('fallback_value', $result);
    }

    public function testGetOptionReturnsNullDefaultForMissingKey()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        $result = $rel->getOption('nonexistent_key');

        $this->assertNull($result);
    }

    public function testGetOptionReturnsConstraintsArray()
    {
        $constraints = ['on_delete' => 'CASCADE', 'on_update' => 'SET NULL'];
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id', ['constraints' => $constraints]);

        $result = $rel->getOption('constraints');

        $this->assertIsArray($result);
        $this->assertEquals('CASCADE', $result['on_delete']);
        $this->assertEquals('SET NULL', $result['on_update']);
    }

    public function testGetConstraintNameWithCustomName()
    {
        $rel = new OneHasMany(
            'Anorm\Test\PostModel',
            'posts',
            'user_id',
            'id',
            ['constraints' => ['constraint_name' => 'my_custom_fk', 'on_delete' => 'CASCADE']]
        );

        $name = $rel->getConstraintName('users');

        $this->assertEquals('my_custom_fk', $name);
    }

    public function testGetConstraintNameAutoGeneratedWithoutCustomName()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        // Auto-generate returns fk_{firstArg}_{foreignKey}; first arg is treated as sourceTable
        $name = $rel->getConstraintName('posts');

        $this->assertEquals('fk_posts_user_id', $name);
    }

    public function testGetConstraintNameAutoGeneratedWithTargetTable()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        // generateForeignKeyConstraints calls getConstraintName($targetTable, $sourceTable)
        // so first arg is 'posts' (the related table), and auto-generate returns fk_posts_user_id
        $name = $rel->getConstraintName('posts', 'users');

        $this->assertEquals('fk_posts_user_id', $name);
    }

    public function testGetConstraintNameWithExplicitTargetTable()
    {
        $rel = new OneHasMany('Anorm\Test\CommentModel', 'comments', 'post_id', 'id');

        $name = $rel->getConstraintName('posts', 'comments');

        $this->assertEquals('fk_posts_post_id', $name);
    }

    // -------------------------------------------------------------------------
    // OneHasMany::generateForeignKeyConstraints
    // -------------------------------------------------------------------------

    public function testGenerateForeignKeyConstraintsReturnsArray()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        $constraints = $rel->generateForeignKeyConstraints('users');

        $this->assertIsArray($constraints);
        $this->assertCount(1, $constraints);
    }

    public function testGenerateForeignKeyConstraintsContainsAlterTableSql()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        $constraints = $rel->generateForeignKeyConstraints('users');
        $sql = $constraints[0];

        $this->assertStringContainsString('ALTER TABLE', $sql);
        $this->assertStringContainsString('ADD CONSTRAINT', $sql);
        $this->assertStringContainsString('FOREIGN KEY', $sql);
        $this->assertStringContainsString('REFERENCES', $sql);
    }

    public function testGenerateForeignKeyConstraintsReferencesSourceTable()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        $constraints = $rel->generateForeignKeyConstraints('users');
        $sql = $constraints[0];

        $this->assertStringContainsString('`posts`', $sql);
        $this->assertStringContainsString('`users`', $sql);
        $this->assertStringContainsString('`user_id`', $sql);
        $this->assertStringContainsString('`id`', $sql);
    }

    public function testGenerateForeignKeyConstraintsIncludesOnDeleteAndOnUpdate()
    {
        $rel = new OneHasMany('Anorm\Test\PostModel', 'posts', 'user_id', 'id');

        $constraints = $rel->generateForeignKeyConstraints('users');
        $sql = $constraints[0];

        $this->assertStringContainsString('ON DELETE RESTRICT', $sql);
        $this->assertStringContainsString('ON UPDATE CASCADE', $sql);
    }

    public function testGenerateForeignKeyConstraintsWithCascadeOptions()
    {
        $rel = new OneHasMany(
            'Anorm\Test\PostModel',
            'posts',
            'user_id',
            'id',
            ['constraints' => ['on_delete' => 'CASCADE', 'on_update' => 'SET NULL']]
        );

        $constraints = $rel->generateForeignKeyConstraints('users');
        $sql = $constraints[0];

        $this->assertStringContainsString('ON DELETE CASCADE', $sql);
        $this->assertStringContainsString('ON UPDATE SET NULL', $sql);
    }

    public function testGenerateForeignKeyConstraintsWithCustomConstraintName()
    {
        $rel = new OneHasMany(
            'Anorm\Test\PostModel',
            'posts',
            'user_id',
            'id',
            ['constraints' => ['constraint_name' => 'fk_custom_name']]
        );

        $constraints = $rel->generateForeignKeyConstraints('users');
